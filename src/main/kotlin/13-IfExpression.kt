fun main() {

    // If Expression

    val examValue = 90

    if(examValue > 80 ){
        println("Lulus")
    }

    // Else Expression

    val nilaiUjian = 60

    if(nilaiUjian > 70) {
        println("lulus")
    } else {
        println("Silahkan coba lagi ")
    }

    // Else If Expression

    val nilai = 70

    if(nilai > 80){
      println("Good Job ")
    } else if (nilai > 60){
        println("Not Bad")
    } else {
        println("silahkan coba lagi")
    }
}